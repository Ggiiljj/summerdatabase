package model;

public class SpecInfo {
	private String peopleid;
	private String specclass;
	private String specgrade;
	private String specdec;
	private String specprove;
	public String getPeopleid() {
		return peopleid;
	}
	public void setPeopleid(String peopleid) {
		this.peopleid = peopleid;
	}
	public String getSpecclass() {
		return specclass;
	}
	public void setSpecclass(String specclass) {
		this.specclass = specclass;
	}
	public String getSpecgrade() {
		return specgrade;
	}
	public void setSpecgrade(String specgrade) {
		this.specgrade = specgrade;
	}
	public String getSpecdec() {
		return specdec;
	}
	public void setSpecdec(String specdec) {
		this.specdec = specdec;
	}
	public String getSpecprove() {
		return specprove;
	}
	public void setSpecprove(String specprove) {
		this.specprove = specprove;
	}
	
}
