package model;

public class UsersInfo {
	private String peopleid;
	private String peoplename;
	private String peoplegrade;
	private String peoplepwd;
	public String getPeopleid() {
		return peopleid;
	}
	public void setPeopleid(String peopleid) {
		this.peopleid = peopleid;
	}
	public String getPeoplename() {
		return peoplename;
	}
	public void setPeoplename(String peoplename) {
		this.peoplename = peoplename;
	}
	public String getPeoplegrade() {
		return peoplegrade;
	}
	public void setPeoplegrade(String peoplegrade) {
		this.peoplegrade = peoplegrade;
	}
	public String getPeoplepwd() {
		return peoplepwd;
	}
	public void setPeoplepwd(String peoplepwd) {
		this.peoplepwd = peoplepwd;
	}
	
}
